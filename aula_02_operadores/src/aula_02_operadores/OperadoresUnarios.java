package aula_02_operadores;

public class OperadoresUnarios {

	public static void main(String[] args) {
		
		int contador = 1000;
		
		System.out.println("Valor inicial do contador " + contador);
		
		System.out.println("Valor pré-incremento:  " + (++ contador));
		
		System.out.println("Valor prós-incremento:  " + (contador ++));
		
		System.out.println("Valor final do contador " + contador);

	}

}
